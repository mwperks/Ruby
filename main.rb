#modulo operator. It gives the remainder when counter is divided by 2.

puts 3 % 2 #1 due to having 1 left over

puts 2 % 2 #0 due to dividing and having none remaining

puts 23 % 3 #2 due to having 2 left over

puts 89 % 9 #8 due to having 8 left over

#So if you divided a number by two, and the integer remainder of that number is one, then you know the number was odd. Your example checks for odd numbers.

